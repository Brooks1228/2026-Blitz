public class Variables {
    static void main() {
        // primitive data types:
        //int, double, char, boolean, string
        int x = 10;
        double y = 10.5;
        char c = 'B';
        boolean valid = false;
        String name = "Yo momma";

        System.out.println("x: " + x + " y: " + y + " c" + c + " valid: " + valid + " name " + name);
        System.out.println(String.format("x: "));
    }
}
